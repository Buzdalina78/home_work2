# описание  моей собаки
# str -строковый тип
name = 'Jackie'
print(type(name))
breed = 'pug'
print(type(breed))

# int- целое число
age = 3
print(type(age))

# float- число с плавающей точкой
weight_kg = 8.2
print(type(weight_kg))

# bool- логический тип
is_dog = True
print(type(is_dog))
is_male = True
print(type(is_male))
