# обьект для описания - студент
# str-строковый тип данных
name = input('введите имя: ')
print(type(name))
last_name = input('введите фамилию: ')
print(type(last_name))
faculty = input('ваш факультет: ')
print(type(faculty))

# int- целое число
age = int(input('ваш возраст: '))
print(type(age))
group_number = int(input('номер группы: '))
print(type(group_number))

# float-число с плавающей точкой
average_score = float(input('ваш средний бал: '))
print(type(average_score))
exact_weight = float(input('ваш точный вес (кг): '))
print(type(exact_weight))

